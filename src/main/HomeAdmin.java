    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ID_Order = (String) orderroom.getValueAt(orderroom.getSelectedRow(), 0);
        String sql = "UPDATE oder SET stt_oder = 'Agreed' WHERE ID_oder = ?;";

        String sqlgettroom = "SELECT * FROM `rooms` WHERE `Number_room` = ?";

        String sqlgetorder = "SELECT * FROM `oder` WHERE ID_oder = ?;";

        String sqluproom = "UPDATE `rooms` SET `Stt_room` = 'Non available', `Booking_person_ID` = ? WHERE `rooms`.`Number_room` = ?;";

        if (ID_Order.equals("")) {
            JOptionPane.showMessageDialog(this, "You have not selected!!");
        } else {
            try {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + Config.DATABASENAME, Config.USER, Config.PASS);
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setInt(1, Integer.parseInt(ID_Order));

                PreparedStatement psgetorder = conn.prepareStatement(sqlgetorder);
                psgetorder.setInt(1, Integer.parseInt(ID_Order));
                ResultSet rsgetorder = psgetorder.executeQuery();
                rsgetorder.next();

                PreparedStatement psgetbookid = conn.prepareStatement(sqlgettroom);
                psgetbookid.setInt(1, Integer.parseInt(rsgetorder.getString("rooms")));
                ResultSet rsgetbookid = psgetbookid.executeQuery();
                rsgetbookid.next();
//                JOptionPane.showMessageDialog(this, rsgetbookid.getString("Booking_person_ID"));

                if (rsgetbookid.getString("Booking_person_ID").equals("") == false | rsgetbookid.getString("Booking_person_ID").equals("0")) {
                    JOptionPane.showMessageDialog(this, "Phng  c ngi t!!");

                } else if (pstmt.executeUpdate() == 1) {
                    PreparedStatement psuproom = conn.prepareStatement(sqluproom);
                    psuproom.setString(1, rsgetorder.getString("ID_User_Oder"));
                    psuproom.setString(2, rsgetorder.getString("rooms"));
                    psuproom.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Successful");

//                    JOptionPane.showMessageDialog(this, "Successful");
                    ID_Order = "";
                    showorder();

                }

            } catch (SQLException ex) {
                Logger.getLogger(HomeAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

