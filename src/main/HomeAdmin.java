    public void showorder() {
        itotalorder = 0;
        String sqlorder = "SELECT * FROM `oder` ORDER BY `oder`.`ID_oder` DESC";

        tableorder = (DefaultTableModel) orderroom.getModel();
        tableorder.setNumRows(0);
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + Config.DATABASENAME, Config.USER, Config.PASS);
            Statement sttorder = conn.createStatement();
            ResultSet rsorder = sttorder.executeQuery(sqlorder);
            while (rsorder.next()) {
                tableorder.addRow(new Object[]{rsorder.getString("ID_oder"), rsorder.getString("fullname"), rsorder.getString("phone"), rsorder.getString("check_in_date"), rsorder.getString("check_in_date"), rsorder.getString("rooms"), rsorder.getString("note"), rsorder.getString("ID_User_Oder"), rsorder.getString("stt_oder"), rsorder.getString("log_oder")});
                itotalorder++;
            }
            conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(HomeAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        ListSelectionModel lmorder = orderroom.getSelectionModel();
        lmorder.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        totalorder.setText(Integer.toString(itotalorder));
    }

